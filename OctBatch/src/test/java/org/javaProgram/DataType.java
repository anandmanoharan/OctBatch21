package org.javaProgram;

public class DataType {

	public static void main(String[] args) {
//byte short int  long	float				double				char				String	
		
		byte a = 12;
		System.out.println("Value of Byte : "+ a);
		short s = 9999;
		System.out.println("Value of Short : "+ s);
		int i = 123456789;
		System.out.println("Value of int : "+ i);
		long l = 98994738299l;
		System.out.println("Value of long : "+ l);
		char c ='#';
		System.out.println("Value of char : "+ c);
		String s2 ="asdfasdf 234234 *(&&(& OPIHNJKK";
		System.out.println("Value of String : "+ s2);
		boolean b = false;
		System.out.println("Value of boolean : "+ b);
		float sal = 123454.2334f;
		System.out.println("Value of float : "+ sal);
		
		
	}

}
